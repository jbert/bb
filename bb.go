type Key [16]byte

func (k Key) String() string {
	return hex.EncodeToString(k[:])
}

func ColsToKey(cols [4]uint32) Key {
	var k [16]byte
	for i, col := range cols {
		bs := WordToBytes(col)
		for j, b := range bs {
			k[i*4+j] = b
		}
	}
	return k
}

func KeyToCols(k Key) [4]uint32 {
	var cols [4]uint32
	for i := range cols {
		j := i * 4
		cols[i] = (uint32(k[j]) << 24) + (uint32(k[j+1]) << 16) + (uint32(k[j+2]) << 8) + (uint32(k[j+3]) << 0)
	}
	return cols
}

type ExpandedKey = [11]Key

func KeyExpansion(k Key) ExpandedKey {
	var expKey [11]Key
	for round := range expKey {
		if round == 0 {
			expKey[0] = k
			continue
		}
		prevCols := KeyToCols(expKey[round-1])
		v := RotWord(prevCols[3])
		v = SubWord(v)
		v ^= prevCols[0]
		v ^= Rcon(byte(round))
		var cols [4]uint32
		for j := range cols {
			if j == 0 {
				cols[j] = v
				continue
			}
			cols[j] = cols[j-1] ^ prevCols[j]
		}
		expKey[round] = ColsToKey(cols)
	}
	return expKey
}

type State [16]byte

func PlainBlockToState(ptxt string) (State, error) {
	var s State
	if len(ptxt) != 16 {
		return s, errors.New(fmt.Sprintf("plain text is not one block, length %d not %d", len(ptxt), 16))
	}
	bs := []byte(ptxt)
	for i := range bs {
		row := i % 4
		col := i / 4
		s[row*4+col] = bs[i]
	}
	return s, nil
}

func ColsToState(cols [4][4]byte) State {
	var state State
	for row := range 4 {
		for col := range 4 {
			state[row*4+col] = cols[col][row]
		}
	}
	return state
}

func (state State) Cols() [4][4]byte {
	var cols [4][4]byte
	for row := range 4 {
		for col := range 4 {
			cols[col][row] = state[row*4+col]
		}
	}
	return cols
}

func (state State) String() string {
	sb := strings.Builder{}
	for row := range 4 {
		sb.WriteString(fmt.Sprintf("%02x %02x %02x %02x\n", state[row*4+0], state[row*4+1], state[row*4+2], state[row*4+3]))
	}
	return sb.String()
}

func (state State) SubBytes() State {
	var ret State
	for i, b := range state {
		ret[i] = sbox_en[b]
	}
	return ret
}

func (state State) ShiftRows() State {
	shiftCol := func(step int, col []byte) []byte {
		var ret [4]byte
		for i, b := range col {
			j := (4 + i - step) % 4
			ret[j] = b
		}
		return ret[:]
	}
	var ret State
	for i := range 4 {
		copy(ret[i*4:(i+1)*4], shiftCol(i, state[i*4:(i+1)*4]))
	}
	return ret
}

