func mixCol(a [4]byte) [4]byte {
	var b [4]byte
	b[0] = multiplication_by_2[a[0]] ^ multiplication_by_3[a[1]] ^ a[2] ^ a[3]
	b[1] = a[0] ^ multiplication_by_2[a[1]] ^ multiplication_by_3[a[2]] ^ a[3]
	b[2] = a[0] ^ a[1] ^ multiplication_by_2[a[2]] ^ multiplication_by_3[a[3]]
	b[3] = multiplication_by_3[a[0]] ^ a[1] ^ a[2] ^ multiplication_by_2[a[3]]
	return b
}

func (state State) MixColumns() State {
	cols := state.Cols()
	var ret [4][4]byte
	for i, col := range cols {
		ret[i] = mixCol(col)
	}
	return ColsToState(ret)
}
